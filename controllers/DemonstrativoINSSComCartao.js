'use strict';

var utils = require('../utils/writer.js');
var DemonstrativoINSSComCartao = require('../service/DemonstrativoINSSComCartaoService');

module.exports.demonstrativoINSSConfPOST = function demonstrativoINSSConfPOST (req, res, next) {
  var authenticationType = req.swagger.params['authenticationType'].value;
  var clientId = req.swagger.params['clientId'].value;
  var token = req.swagger.params['token'].value;
  var body = req.swagger.params['body'].value;
  DemonstrativoINSSComCartao.demonstrativoINSSConfPOST(authenticationType,clientId,token,body)
    .then(function (response) {
      utils.writeJson(res, response);
    })
    .catch(function (response) {
      utils.writeJson(res, response);
    });
};

